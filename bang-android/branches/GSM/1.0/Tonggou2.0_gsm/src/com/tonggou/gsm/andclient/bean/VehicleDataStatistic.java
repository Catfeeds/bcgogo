package com.tonggou.gsm.andclient.bean;

public class VehicleDataStatistic {
	private String appUserNo;
    private float oilWear;
    private float distance;
    private float oilCost;
    private int statYear;
    private int statMonth;
    private long statDate;
    private float oilMoney;
    
	public String getAppUserNo() {
		return appUserNo;
	}
	public void setAppUserNo(String appUserNo) {
		this.appUserNo = appUserNo;
	}
	public float getOilWear() {
		return oilWear;
	}
	public void setOilWear(float oilWear) {
		this.oilWear = oilWear;
	}
	public float getDistance() {
		return distance;
	}
	public void setDistance(float distance) {
		this.distance = distance;
	}
	public float getOilCost() {
		return oilCost;
	}
	public void setOilCost(float oilCost) {
		this.oilCost = oilCost;
	}
	public int getStatYear() {
		return statYear;
	}
	public void setStatYear(int statYear) {
		this.statYear = statYear;
	}
	public int getStatMonth() {
		return statMonth;
	}
	public void setStatMonth(int statMonth) {
		this.statMonth = statMonth;
	}
	public long getStatDate() {
		return statDate;
	}
	public void setStatDate(long statDate) {
		this.statDate = statDate;
	}
	public float getOilMoney() {
		return oilMoney;
	}
	public void setOilMoney(float oilMoney) {
		this.oilMoney = oilMoney;
	}
    
}
